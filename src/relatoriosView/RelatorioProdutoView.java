    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       DefaultTableModel amodel = (DefaultTableModel) jTable1.getModel();
        amodel.setNumRows(0);
        ProdutoDAO proDAO = new ProdutoDAO(); 
        try{
                 
           
           if (jrbAtivos.isSelected() && jrbTodos.isSelected()){       
               listaProduto = proDAO.listaProdutoStatus(0); 
               status = "ATIVO";
               tipoPesquisa = "TODOS";
           }    
           if (jrbInativos.isSelected() && jrbTodos.isSelected()){       
               listaProduto = proDAO.listaProdutoStatus(1);
               status = "INATIVO";
               tipoPesquisa = "TODOS";
           }    
           if (jrbAtivos.isSelected() && jrbEstoqueMinimo.isSelected()){
               listaProduto = proDAO.listaProdutoStatusEstoqueMinimo(0);
               status = "ATIVO";
               tipoPesquisa = "ESTOQUE MÍNIMO";          
           }    
           if (jrbInativos.isSelected() && jrbEstoqueMinimo.isSelected()){
               JOptionPane.showMessageDialog(null, "Está opção não foi desenvolvida pelo programador!!!");
               return;
           }         
           
           if (listaProduto.isEmpty()){
               JOptionPane.showMessageDialog(null, "Nenhum Produto encontrado !!!");
               return;
           }
           
           for (Produto produto : listaProduto){
               amodel.addRow(new Object[]{produto.getId(),produto.getCod_interno(),produto.getDescricao(),produto.getUnidade().getSigla(), produto.getMarca().getDescricao(),produto.getPeso(), produto.getEstoque(), produto.getVlVenda()});            
           }
        }catch (Exception ex){
             JOptionPane.showMessageDialog(null, "Problemas ao Carregar Tabela de Produto"+ex.getMessage());
             ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

