    private void carregaCombobox(){
        ProdutoDAO dao = new ProdutoDAO();
        
        listaProduto = dao.listaProduto();
        
        jcbDescricao.removeAllItems();
        jcbProduto.removeAllItems();
        for (Produto prod : listaProduto){
             jcbDescricao.addItem(prod.getDescricao());
             jcbProduto.addItem(prod);
        }
        
        
        
    }
    
    

