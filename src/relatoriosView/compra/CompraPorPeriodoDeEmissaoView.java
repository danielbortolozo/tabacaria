    private void jbtPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtPesquisaActionPerformed
        DefaultTableModel amodel = (DefaultTableModel) jTable1.getModel();
        amodel.setNumRows(0);
        CompraCabDAO compraDAO = new CompraCabDAO(); 
        Date dataIni, dataFim;
        
        try{
                 
           if (!jtfDataini.getText().equals("")){
               dataIni = df.parse(jtfDataini.getText());
           }else{
               JOptionPane.showMessageDialog(null, "A data inicial está vazia");
               return;
           }          
           if (!jtfDatafim.getText().equals("")){
               dataFim = df.parse(jtfDatafim.getText());               
           }else{
               JOptionPane.showMessageDialog(null, "A data final está vazia");
               return;
           }
           listaCompra = compraDAO.listaPorPeriodoDataEmissao(dataIni, dataFim);
            
            
           if (listaCompra.isEmpty()){
               JOptionPane.showMessageDialog(null, "Não há Compras neste Período");
               return;
           } 
                    
           String emissao_str, subtotal, total;
           for (CompraCab compra : listaCompra){
               emissao_str = df.format(compra.getEmissao());
               subtotal = (new DecimalFormat("#,##0.00").format(compra.getSubtotal()));
               total = (new DecimalFormat("#,##0.00").format(compra.getVltotal()));
               amodel.addRow(new Object[]{compra.getNum_doc(), compra.getDescricao(), compra.getFornecedor().getNome(),
               emissao_str, subtotal, compra.getDesconto(), total});            
           }
        }catch (Exception ex){
             JOptionPane.showMessageDialog(null, "Problemas ao Carregar Tabela de Produto"+ex.getMessage());
             ex.printStackTrace();
        }

    }//GEN-LAST:event_jbtPesquisaActionPerformed

