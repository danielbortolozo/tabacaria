    private void gerarRelatorioDesktop(JRDataSource jrds, String arquivo) {
        try {
            JasperPrint print = JasperFillManager.fillReport(arquivo, null, jrds);
            JasperViewer.viewReport(print, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
 
    public void gerarRelatorio() {
        String arquivo = "src/relatoriosView/compra/fornecedor/report1.jasper";
 
        
 
        JRDataSource jrds = new JRBeanCollectionDataSource(lista);
        gerarRelatorioDesktop(jrds, arquivo);
    }
    
    
    
    
    
