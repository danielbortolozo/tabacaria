    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

       List listaDados = new ArrayList();
       Map parametros = new HashMap();        
       
       Banco banco = new Banco();
       BancoMovimento movBanco = new BancoMovimento();
       float saldo = 0, total=0;
       SaldoContasSelecionadas saldoConta = new SaldoContasSelecionadas();       
       int indexListaMovBanco = 0 ;
     
       for (int i = 0; i < jTable1.getRowCount(); i ++){
           saldo = 0;
           if (jTable1.getValueAt(i, 1).equals(true)){
              banco = listaBanco.get(i);              
              indexListaMovBanco = banco.getBancoMovimentoList().size()-1;
              if (indexListaMovBanco >= 0){ 
                 movBanco = banco.getBancoMovimentoList().get(indexListaMovBanco);
                 saldoConta.setConta(banco.getDescricao());
                 saldoConta.setSaldo(movBanco.getSaldo().floatValue());
                 total = (total + saldoConta.getSaldo());
                 listaDados.add(new SaldoContasSelecionadas(saldoConta.getConta(), saldoConta.getSaldo()));      
              }
           }  
        }
      

       this.empresa = this.empDAO.getEmpresa();
       
       parametros.put("logo", empresa.getLogo());
       
       parametros.put("empresa_telefone", empresa.getTelefone());
       parametros.put("empresa_endereco", empresa.getLogradouro()+" , "+empresa.getNumero()+" - "+empresa.getBairro()
               +" - "+empresa.getCidade()+" - "+empresa.getEstado());
       parametros.put("empresa_nome", empresa.getNomeFantasia());
       parametros.put("empresa_cnpj", empresa.getCnpj());
       parametros.put("empresa_ie", empresa.getIe());
       parametros.put("total", total);
          
       JRBeanCollectionDataSource relat = new JRBeanCollectionDataSource(listaDados);
       JasperPrint jp = null;
       InputStream is = getClass().getResourceAsStream("RelSaldoContaSelecionadas.jasper");    
       if (is == null)
            JOptionPane.showMessageDialog(null, "Relatório não Encontrado");
       try {
            jp = JasperFillManager.fillReport(is, parametros, relat);
        } catch (JRException ex) {
            Logger.getLogger(SaldoContasView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        JasperViewer jv = new JasperViewer(jp, false);
        jv.setDefaultCloseOperation(JasperViewer.DISPOSE_ON_CLOSE);
        jv.setVisible(true);       
    
        
    }//GEN-LAST:event_jButton2ActionPerformed

    
    
