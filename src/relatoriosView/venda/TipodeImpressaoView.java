    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
       if (jrbDetalhado.isSelected()){ 
          ImprimirRelMestreDetalheVenda imprimir = new ImprimirRelMestreDetalheVenda();
          try {
              imprimir.relatorioMestreDetalhe(RelatorioVendaView.listaPedido, df.format(RelatorioVendaView.dataIni), df.format(RelatorioVendaView.dataFim));
              dispose();
          }catch (JRException ex) {
              Logger.getLogger(RelatorioVendaView.class.getName()).log(Level.SEVERE, null, ex);
          }
       }else
           if (jrbSimples.isSelected()){
              ImprimeRelatorio imprimir = new ImprimeRelatorio();
              List<PedidoRel> listaVenda = new ArrayList<>();
              PedidoRel pedidoRel = new PedidoRel();
              // float totalEntrada = 0, totalSaida = 0, totalSaldoFinal = 0;
              String tipo = null, dtIni = null, dtFim = null, total = null;
              String custo=null, lucroBruto=null, desconto=null, receita=null, lucroLiq=null;
              dtIni = df.format(RelatorioVendaView.dataIni);
              dtFim = df.format(RelatorioVendaView.dataFim);
              for (int i = 0; i < RelatorioVendaView.jTable1.getRowCount(); i ++){
                  pedidoRel.setIdVenda((RelatorioVendaView.jTable1.getValueAt(i, 0).toString()));
                  pedidoRel.setDtVenda(RelatorioVendaView.jTable1.getValueAt(i, 1).toString());
                  pedidoRel.setCliente(RelatorioVendaView.jTable1.getValueAt(i, 2).toString());
                  pedidoRel.setTotal((RelatorioVendaView.jTable1.getValueAt(i, 3).toString()));
        
                  listaVenda.add(new PedidoRel(pedidoRel.getIdVenda(), pedidoRel.getCliente(),
                  pedidoRel.getTotal(), pedidoRel.getDtVenda()));
              }
              if (RelatorioVendaView.jrbAVista.isSelected())
                 tipo = "À VISTA";
              else
                  if (RelatorioVendaView.jrbVendaPrazo.isSelected())
                     tipo = "A PRAZO";
                  else
                     if (RelatorioVendaView.jrbTodos.isSelected())
                        tipo = "TODOS";
              total = RelatorioVendaView.jtfTotal.getText();
              custo = RelatorioVendaView.jlCustoTotal.getText();
              lucroBruto = RelatorioVendaView.jlLucroBruto.getText();
              desconto = RelatorioVendaView.jlDesconto.getText();
              receita = RelatorioVendaView.jlReceitaTotal.getText();
              lucroLiq = RelatorioVendaView.jlLucroLiq.getText();
      
              try{
                  imprimir.relatorioVendaPorPeriodo(listaVenda, tipo, dtIni, dtFim, total,
                          custo, lucroBruto, desconto, receita, lucroLiq);
                  dispose();
              } catch (JRException ex) {
                 //Logger.getLogger(RelatorioVendaView.class.getName()).log(Level.SEVERE, null, ex);
                  JOptionPane.showMessageDialog(null, "Error ao gerar o Relatório. Error: "+ex.getMessage());
              } 
           }
    }//GEN-LAST:event_jButton2ActionPerformed

