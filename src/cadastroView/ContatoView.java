    private void jbtImportaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtImportaActionPerformed
        
        
        if (formulario.equals("cliente")){
            amodel = (DefaultTableModel) ClienteView.jTableContato.getModel();
        }else
            if (formulario.equals("fornecedor")){
                amodel = (DefaultTableModel) FornecedorView.jTableContato.getModel(); 
            }else
                if (formulario.equals("colaborador")){
                    amodel = (DefaultTableModel) ColaboradorView.jTableContato.getModel(); 
                }
            
        
        amodel.addRow(new Object[]{jtfDescricao.getText(), jtfDDD.getText(), jtfNumero.getText(), 0L});
        

        limpar();
        dispose();
    }//GEN-LAST:event_jbtImportaActionPerformed

    private void jtfDescricaoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfDescricaoKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == evt.VK_ENTER){
           if (jtfDescricao.equals(null) || jtfDescricao.getText().equals("")){
               JOptionPane.showMessageDialog(null, "O campo descrição não pode ser nullo ou vazio !!!");
           }else{
               jtfDDD.requestFocus();
           }
               
        }
    }//GEN-LAST:event_jtfDescricaoKeyPressed

    private void jtfDDDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfDDDKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == evt.VK_ENTER){
            if (jtfDDD.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "O campo DDD não poder ser nullo ou vazio !!!");
            }else{
                jtfNumero.requestFocus();
            }
                
        }
    }//GEN-LAST:event_jtfDDDKeyPressed

    private void jtfDescricaoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtfDescricaoFocusLost
        // TODO add your handling code here:
      //  jtfDescricao.setText(jtfDescricao.getText().toUpperCase());
         jtfDescricao.setText(RemoverAcentosString.semAcento(jtfDescricao.getText().toUpperCase()));
    }//GEN-LAST:event_jtfDescricaoFocusLost

    public void limpar(){
        jtfDescricao.setText("");
        jtfDDD.setText("");
        jtfNumero.setText("");
    }
    
    private void jtfNumeroKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfNumeroKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == evt.VK_ENTER){
            if (jtfNumero.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "O campo número não pode ser nullo ou vazio !!!");
            }else{
                jbtImporta.setEnabled(true);
                jbtImporta.requestFocus();
            }
            
        }
    }//GEN-LAST:event_jtfNumeroKeyPressed

    private void jbtImportaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jbtImportaKeyPressed

        if (evt.getKeyCode() == evt.VK_ENTER){
            String op = System.getProperty("os.name");
            if (!"Linux".equals(op))
               jbtImportaActionPerformed(null);
        }  
    }//GEN-LAST:event_jbtImportaKeyPressed

