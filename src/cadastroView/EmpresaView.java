    private void jbtInserirImagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtInserirImagemActionPerformed
        JFileChooser fc = new JFileChooser();
        int res = fc.showOpenDialog(null);
        if (res == JFileChooser.APPROVE_OPTION) {
            File arquivo = fc.getSelectedFile();
            try {
                ManipulaImagem.imagem = ManipulaImagem.ImageDimensao(arquivo.getAbsolutePath(), 166, 195);
                lblFoto.setIcon(new ImageIcon(ManipulaImagem.imagem));
                String foto = "" + fc.getSelectedFile().getName();
               // jtfFoto.setText(foto); caminho imagem. foi retirado deste projeto
                jbtLimparImagem.setEnabled(true);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Você não Selecionou Imagem!");
            }
        }
        
    }//GEN-LAST:event_jbtInserirImagemActionPerformed

