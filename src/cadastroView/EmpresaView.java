    private void jbtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalvarActionPerformed
        Empresa empresa = new Empresa();
        
        if (idEmpresa == 1)
            empresa.setId(1);
                
        empresa.setRazaoSocial(jtfRazao.getText());
        empresa.setNomeFantasia(jtfFantasia.getText());
        empresa.setCnpj(jtfCNPJ.getText());
        empresa.setIe(jtfIE.getText());
        empresa.setTelefone(jtfTelefone.getText());
        
        try{
            empresa.setLogo(ManipulaImagem.getImgBytes(ManipulaImagem.imagem));
        }catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Nenhuma Imagem Enviada.: " + ex.getMessage(), "IMAGEM.: ", JOptionPane.INFORMATION_MESSAGE);
        }
        
        empresa.setLogradouro(jtfLogradouro.getText());
        empresa.setNumero(jtfNumero.getText());       
        empresa.setBairro(jtfBairro.getText());
        empresa.setCidade(jtfCidade.getText());
        empresa.setCep(jtfCep.getText());
        empresa.setEstado(jtfEstado.getText());
        
        daoEmp.salvar(empresa);
        if (idEmpresa == 1)
             JOptionPane.showMessageDialog(null, "Empresa Alterada com Sucesso !!!");
        else
           JOptionPane.showMessageDialog(null, "Empresa Cadastrada com Sucesso !!!");
        
       
    }//GEN-LAST:event_jbtSalvarActionPerformed

