    private ObservacoesPedido carregaObjeto(){      
        ObservacoesPedido obs = new ObservacoesPedido();        
        if (operacao == "alterar" )
            obs.setId((Long) jTable1.getValueAt(jTable1.getSelectedRow(), 0));        
        obs.setDescricao(jtfDescricao.getText());  
        
        if (jTable2.getModel().getRowCount() > 0){
           for(int linha=0; linha<jTable2.getRowCount();linha++){
               Long idCat = (Long) jTable2.getModel().getValueAt(linha, 0);
               for (CategoriaProduto catProd : listCategoria){
                   if (idCat == catProd.getId()){
                      obs.getCategoriaProduto().add(catProd);
                      break;
                   }
               }            
            }
        }         
        return obs;
    }
    
