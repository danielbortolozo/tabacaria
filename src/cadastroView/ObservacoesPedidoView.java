    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        operacao = "alterar";
        limpar();
        jtfDescricao.setEnabled(true);
        
        Long id = Long.parseLong(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
        
        for (ObservacoesPedido b : listaObs){
            if (Objects.equals(id, b.getId())){               
                jtfDescricao.setText(b.getDescricao());           
                break;                
            }
        }
        carregarTable2(id);
        
        
              
        jtfDescricao.setText((String) jTable1.getValueAt(jTable1.getSelectedRow(), 1));
        jbNovo.setEnabled(false);
        jbtSalvar.setEnabled(true);
        jbtAddCategoria.setEnabled(true);
        jbtCancelar.setEnabled(true);          
    }//GEN-LAST:event_jTable1MouseClicked

