    private Cep carregaObjeto(){
      
        Cep cep = new Cep();
        
        //Passando os parâmetros para o Objeto Cep.
      //  
        if (operacao == "alterar" )
            cep.setId((Long) jTable2.getValueAt(jTable2.getSelectedRow(), 0));
        cep.setLogadouro(jtfLogradouro.getText());
        cep.setBairro(jtfBairro.getText());
        cep.setCidade(jtfCidade.getText());
        cep.setCep(jtfCep.getText());
        cep.setUf(jcbEstado.getSelectedItem().toString());
    
        return cep;
    }
    
    private void limpar(){
        
        jtfLogradouro.setText("");
        jtfBairro.setText("");
        jtfCep.setText("");
        jtfCidade.setText("");
        jcbEstado.setSelectedIndex(2);
        jtfPesquisa.setText("");
             
    }
