    private void carregaTable(){
        
       // Cep cep = new Cep();
        
      //  String[] tableColumsName = {"CÓD.","LOGRADOURO","BAIRRO"};
        DefaultTableModel amodel = (DefaultTableModel) jTable2.getModel();
     //   amodel.setColumnIdentifiers(tableColumsName);
       // modeloTable = (DefaultTableModel) jTable2.getModel();
        
        amodel.setNumRows(0);
        CepDAO daoCep = new CepDAO();
        
        
        
        listaCep = null;
        
        listaCep = daoCep.getCep();
        
        for (Cep cep : listaCep){
            
          //  jTable2.setValueAt();
            
                    
            amodel.addRow(new Object[]{cep.getId(),cep.getLogadouro(),cep.getBairro(),cep.getCidade(),cep.getCep(),cep.getUf()});
            
            
        }
    }
    
      
    
    

