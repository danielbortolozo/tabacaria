        if (jCheckBox4.isSelected())
            cobranca = true;
        
        if (jtfNumero.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "O campo Número não pode ser nullo ou vazio !!!");
            jtfNumero.requestFocus();
            return;
            
        }
        if (jtfLogradouro.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "O campo Logradouro não pode ser nullo ou vazio !!!");
            jtfLogradouro.requestFocus();
            return;
        }
        if (jtfCidade.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "O campo Cidade não pode ser nullo ou vazio !!!");
            jtfCidade.requestFocus();
            return;
        }
        amodel.addRow(new Object[]{jtfLogradouro.getText(),jtfNumero.getText(),jtfBairro.getText(),
           jtfCidade.getText(), jcbEstado.getSelectedItem().toString(),jtfCep.getText(),        
           principal, correspondencia, entrega, cobranca, 0L});
             
