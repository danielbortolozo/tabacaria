    private void jbtSalvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalvar1ActionPerformed
         
        if (javax.swing.JOptionPane.showConfirmDialog(null, "Deseja Realmente Ativar a Licença?", "ATENÇÂO ", javax.swing.JOptionPane.YES_NO_OPTION) == 0) {
            Licenca l = new Licenca();
            LicencaDAO ldao = new LicencaDAO();
            l.setId(1L);
            l.setPwd(jtfpwd.getText());
            if (!jtfpwd.getText().equals("")){
               ldao.salvar(l);
               JOptionPane.showMessageDialog(null, "Licença salva com sucesso.");
               jbtSairActionPerformed(evt);
            }else{
                JOptionPane.showMessageDialog(null, "Digite uma Chave de Acesso!!!");
                return;
            }
        }
    }//GEN-LAST:event_jbtSalvar1ActionPerformed

