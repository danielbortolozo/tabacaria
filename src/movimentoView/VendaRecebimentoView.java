    private void jbtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalvarActionPerformed
        VendaRecebimentoDAO daoRecebto = new VendaRecebimentoDAO();
        
           
           try{
               
               for (VendaRecebimento v: listaVendaRecebimento){
                   System.out.println("valor ="+v.getVl_recebido());
               }
               
               System.out.println("Tamanho da lista recebimento ="+listaVendaRecebimento.size());
               daoRecebto.salvar(listaVendaRecebimento);
               
               VendaCabView.recebidoStr = String.valueOf(soma);
               VendaCabView.trocoStr = jtfRestante.getText();
               if (listaVendaRecebimento.size() > 1)
                  VendaCabView.tipoPagto = "MULTIPLO";
               if (listaVendaRecebimento.size() == 1)
                   VendaCabView.tipoPagto = jcbTipoPagto.getSelectedItem().toString();
               if (listaVendaRecebimento.size() == 0){
                   JOptionPane.showMessageDialog(null, "Nenhum tipo de Pagamento foi informado para esta Venda.");
                   return;
               }
               transacao = "yes";
           }catch (Exception ex){
               JOptionPane.showMessageDialog(null, "Problemas ao Receber a Venda. Error: "+ex.getMessage());
               transacao = "no";
           }
           dispose();
        
    }//GEN-LAST:event_jbtSalvarActionPerformed

    private VendaRecebimento carregaVendaRecebimento(){
        VendaRecebimento vendaRecebto = new VendaRecebimento();
        
       
        vendaRecebto.setTipoPagamento(listaTipoPagto.get(jcbTipoPagto.getSelectedIndex()));
        vendaRecebto.setDtPagamento(dtSis);
 //       vendaRecebto.setParcela(Integer.parseInt(jlParcela.getText()));
//        vendaRecebto.setJuro(Float.parseFloat(jtfJuros.getText()));
        vendaRecebto.setDesconto(Float.parseFloat((jtfDesconto.getText())));
        vendaRecebto.setVl_subtotal(subtotal);
        vendaRecebto.setVl_total(vltotal);
        vendaRecebto.setVl_recebido(vlrecebido);
        vendaRecebto.setVl_troco(troco);
        vendaRecebto.setVenda(venda);        
        
        if (tipoVenda == 0)//Venda A Vista.
            vendaRecebto.setDtVencimento(dtSis);
        
        return vendaRecebto;           
    }
    
    
