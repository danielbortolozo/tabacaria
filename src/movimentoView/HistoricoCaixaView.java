    private void jbPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisaActionPerformed
        DefaultTableModel amodel = (DefaultTableModel) jTable1.getModel();
        amodel.setNumRows(0);
        CaixaDAO caixaDao = new CaixaDAO();        
        Date dataIni, dataFim;
        try{

            if (jrbPesquisaPeriodo.isSelected()){
               if (!jftDataIni.getText().equals("")){
                  dataIni = df.parse(jftDataIni.getText());
               }else{
                  JOptionPane.showMessageDialog(null, "A data inicial está vazia");
                  return;
               }
               if (!jftDataFim.getText().equals("")){
                  dataFim = df.parse(jftDataFim.getText());
               }else{
                  JOptionPane.showMessageDialog(null, "A data final está vazia");
                  return;
               }
            }else{
                  if (jrbExibirMaisRecent.isSelected()){
                     if (jcbUsuario.getSelectedItem().equals("TODOS"))
                        listaCaixa = caixaDao.listaCaixaTodosUsuario();
                     else
                        listaCaixa = caixaDao.listaCaixaPorUsuario(listaUsuario.get(jcbUsuario.getSelectedIndex()));
                  }
            }            
            if (listaCaixa.isEmpty()){
                JOptionPane.showMessageDialog(null, "Este Usuário, não têm Registros no CAIXA");
                return;
            }        
            
            //Popular jTable
            String dtAbertura = null;
            String dtFechado = null;
            BigDecimal saldoInicial = new BigDecimal(BigInteger.ZERO);
            CaixaItens itensCaixa = new CaixaItens();
            for (Caixa caixa : listaCaixa){            
                
                //Formatar e converter data.
                dtAbertura = sdfDH.format(caixa.getDtAbertura());
                //Verifica se a data é vazia.
                if (caixa.getDtFechamento() == null)
                    dtFechado = "";
                else
                   dtFechado = sdfDH.format(caixa.getDtFechamento());
                //Pegar Saldo Inicial na Tabela Detalhe do Caixa.
                itensCaixa = caixa.getCaixaItensList().get(0);
                saldoInicial = (itensCaixa.getVlEntrada());

//              pagtoRel.setVencimento(df.format(pagto.getVencimento()));
//              pagtoRel.setParcelas(String.valueOf(pagto.getPagamentoCab().getQtd_parcela()));
//              pagtoRel.setTotal(new DecimalFormat("#,##0.00").format(pagto.getPagamentoCab().getVl_total()));
//              pagtoRel.setNumParcela(String.valueOf(pagto.getNum_parcela()));
//                       
//              pagtoRel.setVlParcela(new DecimalFormat("#,##0.00").format(pagto.getVl_parcela()));
//                
                amodel.addRow(new Object[]{dtAbertura, dtFechado, caixa.getTotalEntrada(), caixa.getTotalSaida(),
                saldoInicial, caixa.getSaldoFinal(), caixa.getIdUsuario().getNome(), caixa.getStatus(), caixa.getObs(), caixa.getId()});
           
            }
//             DefaultTableCellRenderer coluna = new DefaultTableCellRenderer();
//             jTable1.getColumnModel().getColumn(2).setCellRenderer(coluna);
              
            
            
        }catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Problemas ao Carregar Histórico do Caixa"+ex.getMessage());
            ex.printStackTrace();
        }       
    }//GEN-LAST:event_jbPesquisaActionPerformed

