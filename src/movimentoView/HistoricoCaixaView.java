    private void jbtImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtImprimirActionPerformed
     //   CaixaDAO caixaDAO = new CaixaDAO();
        ImprimeRelatorio imprimir = new ImprimeRelatorio();
        List<RelCaixa> listaHistCaixa = new ArrayList<>();
        RelCaixa relHistCaixa = new RelCaixa();
        float totalEntrada = 0, totalSaida = 0, totalSaldoFinal = 0;
        for (int i = 0; i < jTable1.getRowCount(); i ++){
            
            relHistCaixa.setAberto(jTable1.getValueAt(i, 0).toString());
            relHistCaixa.setFechado(jTable1.getValueAt(i, 1).toString());
            relHistCaixa.setTotalEntrada(jTable1.getValueAt(i, 2).toString());
            relHistCaixa.setTotalSaida(jTable1.getValueAt(i, 3).toString());
            relHistCaixa.setSaldoFinal(jTable1.getValueAt(i, 5).toString());
            relHistCaixa.setColaborador(jTable1.getValueAt(i, 6).toString());
            
            totalEntrada = totalEntrada + Float.parseFloat(jTable1.getValueAt(i, 2).toString());
            totalSaida = totalSaida + Float.parseFloat(jTable1.getValueAt(i, 3).toString());           
            totalSaldoFinal = totalSaldoFinal + Float.parseFloat(jTable1.getValueAt(i, 5).toString());
           
            
            listaHistCaixa.add(new RelCaixa(relHistCaixa.getAberto(), relHistCaixa.getFechado(),
            relHistCaixa.getTotalEntrada(), relHistCaixa.getTotalSaida(), relHistCaixa.getSaldoFinal(),
            relHistCaixa.getColaborador()));
        }
           
        String totalEntStr = String.valueOf(new DecimalFormat("#,##0.00").format(totalEntrada));
        String totalSaiStr = String.valueOf(new DecimalFormat("#,##0.00").format(totalSaida));
        String totalSaldoStr = String.valueOf(new DecimalFormat("#,##0.00").format(totalSaldoFinal));
        
        try {
            imprimir.historicoCaixa(listaHistCaixa, totalEntStr, totalSaiStr, totalSaldoStr);
        } catch (JRException ex) {
            Logger.getLogger(HistoricoCaixaView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jbtImprimirActionPerformed

