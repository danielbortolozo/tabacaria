    private PagamentoCab carregaObjeto() throws ParseException{
        PagamentoCab pagtoCab = new PagamentoCab();
        PagamentoDet pagtoDet = new PagamentoDet();
        List<PagamentoDet> listPagtoDet = new ArrayList<PagamentoDet>();
        Calendar calendar = Calendar.getInstance(); 
        float vlParcela=0;            
        if (operacao == "salvar"){
           pagtoCab.setDescricao(jtfDescricao.getText());
           SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
           pagtoCab.setEmissao(sdf.parse(jtfDtEmissao.getText()));
           pagtoCab.setNumdoc(jtfNumDoc.getText());
           pagtoCab.setFornecedor(listFornecedor.get(jcbFornecedor.getSelectedIndex()));
           pagtoCab.setVencimento(sdf.parse(jtfDtVencimento.getText()));
           pagtoCab.setStatus(jcbStatus.getSelectedItem().toString());
           pagtoCab.setQtd_parcela(Integer.parseInt(jtfQtdeParcelas.getText()));
           pagtoCab.setSubtotal(Float.parseFloat(ValorStrParaFloat(jtfSubTotal.getText())));
           
           pagtoCab.setDesconto(Float.parseFloat(ValorStrParaFloat(jtfDescontoCab.getText())));
           pagtoCab.setJuro(Float.parseFloat(ValorStrParaFloat(jtfJurosCab.getText())));          
           pagtoCab.setVl_total(Float.parseFloat(ValorStrParaFloat(jtfVlTotal.getText())));            
           vlParcela = (pagtoCab.getVl_total()/pagtoCab.getQtd_parcela());           
        
           //Salvando Categoria Conta
           pagtoCab.setId_categoria(listCategoriaConta.get(jcbCategoria.getSelectedIndex()));
                   
           if (pagtoCab.getQtd_parcela()!= 0){
               calendar.setTime(pagtoCab.getVencimento());               
               for (int i = 1; i <= pagtoCab.getQtd_parcela(); i ++){
                  
                   if (jcbTipoPagto.getSelectedIndex() == -1){
                       jcbTipoPagto.setSelectedItem("NENHUM");
                       pagtoDet.setTipo_pagamento(listTipoPagto.get(jcbTipoPagto.getSelectedIndex()));
                   }else
                       pagtoDet.setTipo_pagamento(listTipoPagto.get(jcbTipoPagto.getSelectedIndex()));
                   if (jcbBanco.getSelectedIndex() == -1){
                       jcbBanco.setSelectedItem("NENHUM");
                       pagtoDet.setBanco(listBanco.get(jcbBanco.getSelectedIndex()));
                   }else
                       pagtoDet.setBanco(listBanco.get(jcbBanco.getSelectedIndex()));
                   
                   pagtoDet.setJuro(0);
                   pagtoDet.setDesconto(0);
                   pagtoDet.setDt_pagamento(null);
                   pagtoDet.setMulta(0);
                   pagtoDet.setNum_parcela(i);
                   pagtoDet.setVencimento(calendar.getTime());
                   pagtoDet.setVl_parcela(vlParcela); 
                   
                   listPagtoDet.add(new PagamentoDet(pagtoDet.getTipo_pagamento(),pagtoDet.getBanco(), pagtoDet.getDesconto(),
                   pagtoDet.getJuro(), pagtoDet.getMulta(), pagtoDet.getVl_parcela(), pagtoDet.getVl_pago(),
                   pagtoDet.getNum_parcela(), pagtoDet.getVencimento(), pagtoDet.getDt_pagamento()));
                   calendar.add(Calendar.MONTH, 1);
                   pagtoDet.setVencimento(calendar.getTime());
               }
               pagtoCab.setListaPagamentoDet(listPagtoDet);
           }
           pagtoCab.setSaldo_devedor(pagtoCab.getVl_total());          
        }
            
        return pagtoCab;
    }
    public void alterar() throws ParseException{
        Calendar calendar = Calendar.getInstance();
        
        if (operacao == "alterar"){
                String vlpg_str = jtfVlPago.getText();
                vlpg_str = ValorStrParaFloat(vlpg_str);
                String vlparc_str = jtfVlParcela.getText();
                vlparc_str = ValorStrParaFloat(vlparc_str);
                calendar.setTime(pgtDet.getVencimento());
                pgtCab.setId_categoria(listCategoriaConta.get(jcbCategoria.getSelectedIndex()));
                pgtCab.setDescricao(jtfDescricao.getText());
                
                
                
                
                pgtDet.setTipo_pagamento(listTipoPagto.get(jcbTipoPagto.getSelectedIndex()));
                pgtDet.setBanco(listBanco.get(jcbBanco.getSelectedIndex()));
                pgtDet.setJuro(Float.parseFloat(jtfJurosDet.getText()));
                pgtDet.setDesconto(Float.parseFloat(jtfDescontoDet.getText()));
                pgtDet.setVencimento(sdf.parse(jtfDtVencimento.getText()));
                
                pgtDet.setVl_parcela(Float.parseFloat(vlparc_str));
                
                if (jtfMulta.getText().equals(""))
                    pgtDet.setMulta(0);
                else
                    pgtDet.setMulta(Float.parseFloat(jtfMulta.getText()));                   
                pgtDet.setVl_pago(Float.parseFloat(vlpg_str));
                pgtDet.setVl_parcela(Float.parseFloat(vlparc_str));
                
                
                Banco bc = new Banco();
                BancoMovimento bm = new BancoMovimento();
                BancoMovimentoDAO bmDAO = new BancoMovimentoDAO();
                bc = listBanco.get(jcbBanco.getSelectedIndex());
                  
                if (!jcbTipoPagto.getSelectedItem().equals("NENHUM") && (!jcbBanco.getSelectedItem().equals("NENHUM")))
                   if (bc.getBancoMovimentoList().isEmpty()){
                      JOptionPane.showMessageDialog(null, "Esta Conta não têm Movimentação !!!");
                      return;                   
                    }
                
                
                
                if (pgtDet.getVl_pago() > 0){                    
                       pgtDet.setDt_pagamento(sdf.parse(jtfDtPagto.getText()));
                       pgtCab.setSaldo_devedor((pgtCab.getSaldo_devedor())-(pgtDet.getVl_parcela()));                    
                       if (jTable2.getRowCount() ==  Integer.parseInt(jtfNumParcela.getText())){
                           JOptionPane.showMessageDialog(null, "Parabéns você está pagando a Última Parcela.");
                           pgtCab.setVencimento(calendar.getTime());
                           pgtCab.setStatus("FECHADO");
                       }else{                    
                           calendar.add(Calendar.MONTH, 1);
                           pgtCab.setVencimento(calendar.getTime());
                       }
                        //Adicionar Debito em Conta Corrente
                        int ultimoRegistro = 0;
                        BigDecimal saldo;
                        BigDecimal debito;
                        ultimoRegistro = bc.getBancoMovimentoList().size();
                        ultimoRegistro = (ultimoRegistro -1);
               
                        saldo = bc.getBancoMovimentoList().get(ultimoRegistro).getSaldo();
                        debito = new BigDecimal(vlpg_str);
                     
                        //Subtrair Debito                   
                        saldo = (saldo.subtract(debito));
                    
                        bm.setIdBanco(bc);
                        bm.setVlSaida(debito);
                        bm.setSaldo(saldo);
                        bm.setObs("Conta:"+jtfCodigo.getText()+" Nº Parc. ("+jtfNumParcela.getText()+") - "+jtfDescricao.getText());
                        bm.setDtLancamento(sdf.parse(jtfDtPagto.getText()));
                        bmDAO.salvar(bm);         
                       
                }
                
               
          //      if (!jcbTipoPagto.getSelectedItem().equals("NENHUM")){
                    
                                      
                    
                         

                pagtoDAO.alterar(pgtCab, pgtDet);
                JOptionPane.showMessageDialog(null, "Conta à Pagar, Alterada com Sucesso!!!");
                
                
                
            }
    } 
    
    
    private String ValorStrParaFloat(String valor){
        valor = valor.replace(".", "");
        valor = valor.replace(",", ".");
        return valor;        
    }
            
    
    
    private void carregaCombobox(){
        fornecedorDAO = new FornecedorDAO();          
        tipoPagtoDAO = new TipoPagamentoDAO();
        categoriaContaDAO = new CategoriaContaDAO();
        bancoDAO = new BancoDAO();
        listFornecedor = fornecedorDAO.listaFornecedor();
        listTipoPagto = tipoPagtoDAO.listTipoPag();
        listBanco = bancoDAO.listBanco();
        listCategoriaConta = categoriaContaDAO.lista();
        
        //Carrega Combobox Fornecedor.
        jcbFornecedor.removeAllItems();
//        for(Pessoa pessao : listFornecedor){
//            System.out.println("Lista fornecedor"+pessao.getId());
//            System.out.println("nome "+pessao.getNome());
//        }
//        
        for (Pessoa fornecedor : listFornecedor){
            jcbFornecedor.addItem(fornecedor.getNome());
        }                     
        jcbTipoPagto.removeAllItems();
        for (TipoPagamento tipoPagto : listTipoPagto){
            jcbTipoPagto.addItem(tipoPagto.getDescricao());
        }
        jcbBanco.removeAllItems();
        for (Banco banco : listBanco){
            jcbBanco.addItem(banco.getDescricao());
        }
        
        jcbCategoria.removeAllItems();
        for (CategoriaConta cat : listCategoriaConta){
            jcbCategoria.addItem(cat.getDescricao());
        }
    }
    
    
