    private void jbtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalvarActionPerformed

        
        if (operacao == "salvar"){
            try {
                if (jcbFornecedor.getSelectedIndex()== -1){
                    JOptionPane.showMessageDialog(null, "Fornecedor precisa ser preenchido !!!");
                    return;
                }
                if (jtfQtdeParcelas.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "Quantidade de parcelas precisa ser preenchido");
                    return;
                }   
                
                
                pagtoDAO.salvar(carregaObjeto());
                JOptionPane.showMessageDialog(null, "Conta à Pagar, Salva com Sucesso!!!");
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null, "Problemas ao Salvar Pagamento! Error:"+ex.getMessage());
            }
        }else
            if (operacao == "alterar"){
                if (!(jtfVlPago.getText().equals("0.0"))&&(jcbTipoPagto.getSelectedItem().equals("NENHUM"))){
                    JOptionPane.showMessageDialog(null, "Tipo de Pagamento Inválido");
                    return;
                }
                if (jtfDtPagto.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "Data do Pagamento está vazia!!!");
                    jtfDtPagto.requestFocus();
                    return;
                }
                try {
                    alterar();
                }catch (ParseException ex) {
                    JOptionPane.showMessageDialog(null, "Problemas ao Salvar Pagamento! Error:"+ex.getMessage());
                }               
            }
        jbtCancelaActionPerformed(evt);
    }//GEN-LAST:event_jbtSalvarActionPerformed

