    private void jbtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalvarActionPerformed
       PedidoTrocaDevolucaoDAO pTrocaDao = new PedidoTrocaDevolucaoDAO();
       PedidoTrocaDevolucao pTroca = new PedidoTrocaDevolucao();
       String vlDevolucaoStr = null;
       String vlTrocaStr = null;
       String vlCreditoStr = null;
       double vlDev = 0;
       Date y = new Date();
       //Atualizar Status dos Itens do Pedido.
        jbtCalcularActionPerformed(null);
       for (int i = 0; i < jTable1.getRowCount(); i ++){
           pedido.getListaItens().get(i).setStatus(jTable1.getValueAt(i, 5).toString());          
       }
       //Salvar Atualização
       pDAO.alterar(pedido);       
       //Tirar virgula e inserir ponto, dos respectivos valores abaixo:       
       vlDevolucaoStr = jtfVlDevolucao.getText();
       vlDevolucaoStr = vlDevolucaoStr.replace(".", "");
       vlDevolucaoStr = vlDevolucaoStr.replace(",", ".");
       
       vlTrocaStr = jtfVlTroca.getText();
       vlTrocaStr = vlTrocaStr.replace(".", "");
       vlTrocaStr = vlTrocaStr.replace(",", ".");
       
       vlCreditoStr = jtfCredito.getText();
       vlCreditoStr = vlCreditoStr.replace(".", "");
       vlCreditoStr = vlCreditoStr.replace(",", ".");
       
       //Salvar Credito do Pedido de troca.
       pTroca.setPedido(pedido);
       pTroca.setUtilizouCredito("NAO");
       pTroca.setVldevolucao(new BigDecimal(vlDevolucaoStr));
       pTroca.setVltroca(new BigDecimal(vlTrocaStr));
       pTroca.setVlcredito(new BigDecimal(vlCreditoStr));
       pTroca.setDataCad(y);
       pTrocaDao.salvar(pTroca);
       
       //Mensagem.       
       JOptionPane.showMessageDialog(null, "Troca / Devolução efetuada com Sucesso !!!");
       jbtCancelarActionPerformed(evt);
        
        
        
    }//GEN-LAST:event_jbtSalvarActionPerformed

