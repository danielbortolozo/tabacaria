     private void carregaTable(Pedido p){
        
        amodel1 = (DefaultTableModel) jTable1.getModel();
        amodel2 = (DefaultTableModel) jTable2.getModel();
        
        
        amodel1.setNumRows(0);
        amodel2.setNumRows(0);
       // amodel3.setNumRows(0);
       
       vlTotal = 0;
        double subtotal = 0;
        int idPedido = 0;
        String dataPagamento = null;
         
        for (PedidoItens itens : p.getListaItens()){
            amodel1.addRow(new Object[]{itens.getQuantidade().toString(),itens.getProduto().getUnidade().getSigla(),
            itens.getProduto().getDescricao(), itens.getVlUnit(), itens.getVlSubtotal(), itens.getStatus()});                         
        }
        
        for (PedidoPagamento pg : p.getListaPagamento()){
            subtotal = Double.parseDouble(pg.getTotal().toString());
            vlTotal = (vlTotal + subtotal);
            dataPagamento = sdfDH.format(pg.getDtPagamento());
            amodel2.addRow(new Object[]{dataPagamento, pg.getAcrescimo(), pg.getDesconto(), pg.getTotal()});
        }
        jtfTotal.setText(new DecimalFormat("#,##0.00").format(vlTotal));
        
    }
    
    
    
    
    
