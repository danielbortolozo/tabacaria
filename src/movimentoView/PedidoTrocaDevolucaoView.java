    private void jtfCodVendaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfCodVendaKeyPressed
        
        if (evt.getKeyCode() == evt.VK_ENTER){
           
           int idPedido = 0;
           jbtAdicionarProduto.setEnabled(false);
          
           jbtCalcular.setEnabled(false);
           jbtSalvar.setEnabled(false);
           
           idPedido = Integer.parseInt(jtfCodVenda.getText());
            
           pedido = pDAO.pedidoId(idPedido);
           
           if (pedido != null){              
              if (pedido.getCliente().getNome().equals("ADMIN")) 
                 jtfCliente.setText("CONSUMIDOR");
              else
                 jtfCliente.setText(pedido.getCliente().getNome());
              jtfData.setText(sdf.format(pedido.getDtCad()));
              carregaTable(pedido);
              jbtCancelar.setEnabled(true);
              jbtAdicionarProduto.setEnabled(true);
              jbtCalcular.setEnabled(true);               
              jbtCalcularActionPerformed(null);
              jbtSalvar.setEnabled(false);
           }else
               JOptionPane.showMessageDialog(null, "Códio de Venda, não encontrado.");
           
            
            
            
        }
    }//GEN-LAST:event_jtfCodVendaKeyPressed

