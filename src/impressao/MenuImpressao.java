    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jrbMatricial.isSelected()){
            
            if (tipoForm == "OrcamentoCabView"){
                ImpressaoMatricial impressao = new ImpressaoMatricial();
                impressao.orcamentoCabMatricial(orcCab, listaCliente, index);
                             
            }
            
        }else
            if (jrbLaser.isSelected()){
                if (tipoForm == "OrcamentoCabView"){
                    ImprimeRelatorio imprimir;
                    try {
                        imprimir = new ImprimeRelatorio();
                        System.out.println("orcCab ="+orcCab);
                        imprimir.orcamento(orcCab, cliente, operador);
                    } catch (ParseException ex) {
                        JOptionPane.showMessageDialog(null, "Problema ao Imprimir Condicional!!! Error:"+ex.getMessage());
                    } catch (Exception ex) {
                        Logger.getLogger(MenuImpressao.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                
            }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

