    private static String carregaArquivo(String arquivo)
            throws FileNotFoundException, IOException{
        
        File file = new File(arquivo);
        if (!file.exists()){
            JOptionPane.showMessageDialog(null, "Arquivo inexistente!");
        }
        
        BufferedReader br = new BufferedReader(new FileReader(arquivo));
        StringBuffer bufSaida = new StringBuffer();
        String linha;
        //jLabel1.setText("<html>");
        while ((linha = br.readLine()) != null){
            bufSaida.append(linha + "\n");
         //   jTextArea1.setText(jLabel1.getText()+linha+"<br>");
        }
       // jLabel1.setText(jLabel1.getText()+"</html>");
        br.close();
        return bufSaida.toString();
    }
    
