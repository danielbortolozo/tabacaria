package model;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

/**
 *
 * @author del
 */
@Entity
@Table(name = "categoria_conta")
public class CategoriaConta implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Column(name = "descricao")
    private String descricao;
   
    @JoinColumn(name = "id_sub_categoria_conta", referencedColumnName = "id")
    @ManyToOne
    private SubCategoriaConta idSubCategoriaConta;

    public CategoriaConta() {
    }

    public CategoriaConta(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public SubCategoriaConta getIdSubCategoriaConta() {
        return idSubCategoriaConta;
    }

    public void setIdSubCategoriaConta(SubCategoriaConta idSubCategoriaConta) {
        this.idSubCategoriaConta = idSubCategoriaConta;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof CategoriaConta)) {
            return false;
        }
        CategoriaConta other = (CategoriaConta) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "model.CategoriaConta[ id=" + id + " ]";
    }
    
}
